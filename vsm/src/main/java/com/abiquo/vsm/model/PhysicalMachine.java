package com.abiquo.vsm.model;

public class PhysicalMachine
{
    private Integer id;

    private String address;

    private String type;

    private String username;

    private String password;

    private VirtualMachinesCache virtualMachines = new VirtualMachinesCache();

    public Integer getId()
    {
        return id;
    }

    public void setId(final Integer id)
    {
        this.id = id;
    }

    public String getAddress()
    {
        return address;
    }

    public void setAddress(final String address)
    {
        this.address = address;
    }

    public String getType()
    {
        return type;
    }

    public void setType(final String type)
    {
        this.type = type;
    }

    public String getUsername()
    {
        return username;
    }

    public void setUsername(final String username)
    {
        this.username = username;
    }

    public String getPassword()
    {
        return password;
    }

    public void setPassword(final String password)
    {
        this.password = password;
    }

    public VirtualMachinesCache getVirtualMachines()
    {
        return virtualMachines;
    }

    public void setVirtualMachines(final VirtualMachinesCache virtualMachines)
    {
        this.virtualMachines = virtualMachines;
    }
}
